int main() {
    ll n, x;
    cin >> n >> x;

    // - Note: 2回目の反射までの軌跡の長さは必ずnになる
    ll ans = n;
    ll a = x, b = n - x;
    if (a < b) swap(a, b);

    for (;;) {
        if (a % b == 0) {
            ans += b * (2 * (a / b) - 1);
            break;
        } else {
            ans += b * (2 * (a / b));
            a %= b;
            swap(a, b);
        }
    }

    printf("%lld\n", ans);
    return 0;
}