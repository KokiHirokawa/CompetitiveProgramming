inline int newline() { putchar('\n'); return 0; }

int main() {
    int n;
    cin >> n;

    string s;
    cin >> s;

    int a = 0, b = n - 1;
    while (a <= b) {
        bool left = false;
        for (int i = 0; a + i <= b; i++) {
            if (s[a + i] < s[b - i]) {
                left = true;
                break;
            } else if (s[a + i] > s[b - i]) {
                left = false;
                break;
            }
        }
        if (left) putchar(s[a++]);
        else putchar(s[b--]);
    }

    putchar('\n');
    return 0;
}